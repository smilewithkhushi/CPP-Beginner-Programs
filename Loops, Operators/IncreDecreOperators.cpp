#include <iostream>
using namespace std;

int main(){

    cout<<"** POST AND PRE INCREMENT AND DECREMENT OPERATORS **"<<endl;

    cout<<endl<<"__POST INCREMENT, DECREMENT OPERATORS__"<<endl;
    cout<<endl;

    int a=15;
    cout<<"Value of a : "<<a<<endl;
    cout<<"Value of a++ : "<<a++<<endl;
    cout<<"NEW Value of a : "<<a<<endl;
    cout<<endl;

    int b=10;
    cout<<"Value of b : "<<b<<endl;
    cout<<"Value of b-- : "<<b--<<endl;
    cout<<"NEW Value of b : "<<b<<endl;
    cout<<endl;

    cout<<endl<<"__PRE INCREMENT, DECREMENT OPERATORS__"<<endl;
    cout<<endl;

    int c=20;
    cout<<"Value of c : "<<c<<endl;
    cout<<"Value of ++c : "<<++c<<endl;
    cout<<"NEW Value of c : "<<c<<endl;
    cout<<endl;


    int d=30;
    cout<<"Value of d : "<<d<<endl;
    cout<<"Value of --d : "<<--d<<endl;
    cout<<"NEW Value of d : "<<d<<endl;
    cout<<endl;




    return 0;
}